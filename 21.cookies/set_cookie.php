<?php
$cookieName = "TestCookie";
$cookieValue = "this is a test cookie... it works";
//set the cookie expiration date to be one hour (i.e. 3600 seconds) in the future
$cookieExpiration = time()+3600; //time() is a built-in PHP function that returns the current time in seconds

//set a cookie, and give it a name, value, and expiration date (optional)
setcookie($cookieName, $cookieValue, $cookieExpiration);

?>
